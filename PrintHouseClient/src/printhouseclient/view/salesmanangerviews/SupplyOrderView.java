    private void addItemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addItemButtonActionPerformed

        Item item = (Item) itemCombo.getSelectedItem();
        boolean itemExist = false;
        String itemId = Integer.toString(item.getItemId());
        for (int i = 0; i < orderTable.getRowCount(); i++) {
            if (itemId.equals(tableModel.getValueAt(i, 0).toString())) {
                itemExist = true;
                break;
            }
        }
        if (!itemExist) {
            if (!quantityText.getText().isEmpty()) {

                String description = item.getDescription();
                String quantity = quantityText.getText();
                String price = priceText.getText();
                String amount = Double.toString(Double.parseDouble(quantity) * Double.parseDouble(price));

                Object row[] = {itemId, description, quantity, price, amount};
                tableModel.addRow(row);
            } else {
                JOptionPane.showMessageDialog(this, "Please enter quantity.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "This item already added to the order.");
        }

        quantityText.setText(null);
        quantityText.requestFocus();

// TODO add your handling code here:
    }//GEN-LAST:event_addItemButtonActionPerformed

