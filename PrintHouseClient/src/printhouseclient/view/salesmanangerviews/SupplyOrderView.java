    private void initSuppliers() {
        try {
            ArrayList<Supplier> supplierList = supplierController.getSupplierList();
            supplierCombo.removeAllItems();
            supplierCombo.addItem(null);
            if (supplierList != null) {
                for (Supplier supplier : supplierList) {
                    supplierCombo.addItem(supplier);
            


                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(SupplyOrderView.class
                    .getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(SupplyOrderView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void initDate() {
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        Calendar cal = Calendar.getInstance();
        dateText.setText(dateFormat.format(cal.getTime()));

    }

    private void initItems() {

        try {
            ArrayList<Item> itemList = itemController.getAllItems();
            itemCombo.removeAllItems();
            itemCombo.addItem(null);
            if (itemList != null) {
                for (Item item : itemList) {
                    itemCombo.addItem(item);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(SupplyOrderView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(SupplyOrderView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void setUpdateTable(Object[] row) {
        tableModel.removeRow(orderTable.getSelectedRow());
        tableModel.addRow(row);
    }
