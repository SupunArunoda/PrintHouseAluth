    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        int selectedRow = orderTable.getSelectedRow();

        if (selectedRow != -1) {
            int result = JOptionPane.showConfirmDialog(this, "Are you sure to remove this Item ?", "Remove Item", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                tableModel.removeRow(orderTable.getSelectedRow());
            }
        }

// TODO add your handling code here:
    }//GEN-LAST:event_removeButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        Supplier supplier = (Supplier) supplierCombo.getSelectedItem();
        int supplyOrderId = Integer.parseInt(supplyOrderIdText.getText());
        
        ArrayList<SupplyOrderDetail> supplyOrderDetailsList = new ArrayList<>();
        for (int i = 0; i < orderTable.getRowCount(); i++) {
            double quantity = Double.parseDouble(tableModel.getValueAt(i, 2).toString());
            double price = Double.parseDouble(tableModel.getValueAt(i, 3).toString());
            int itemId = Integer.parseInt(tableModel.getValueAt(i, 0).toString());
            SupplyOrderDetail supplyOrderDetail = new SupplyOrderDetail(supplyOrderId, quantity, price, supplier.getSupplierId(), itemId);
            
            supplyOrderDetailsList.add(supplyOrderDetail);
            
        }
        
        SupplyOrder supplyOrder = new SupplyOrder(supplyOrderId, "OrderDescription", dateText.getText(), supplier.getSupplierId(), supplyOrderDetailsList);
        
        try {
            int result = supplyOrderController.addSupplyOrder(supplyOrder);
            if(result==1){
                JOptionPane.showMessageDialog(this, "Order Added.");
            }
            
            // TODO add your handling code here:
        } catch (RemoteException ex) {
            Logger.getLogger(SupplyOrderView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SupplyOrderView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButtonActionPerformed


